/** Apply all tokens (brand + RFQ) to an HTML string. */
function applyTokens(html, extra = {}) {
  let out = html;
  const allTokens = { ...BRAND_TOKENS, ...RFQ_TOKENS, ...extra };
  for (const [token, value] of Object.entries(allTokens)) {
    out = out.replaceAll(token, value ?? "");
  }
  // Remove conditional blocks for empty brand images
  out = out.replaceAll(/\{\{#if [^}]+\}\}[\s\S]*?\{\{\/if\}\}/g, (block) => {
    const match = block.match(/\{\{#if ([^}]+)\}\}([\s\S]*?)\{\{\/if\}\}/);
    if (!match) return block;
    const tokenKey = `{{${match[1]}}}`;
    return allTokens[tokenKey] ? match[2] : "";
  });
  return out;
}

/** Generate a QR code as a base64 PNG data URL. */
function buildQrDataUrl(url) {
  return QRCode.toDataURL(url, {
    type: "image/png",
    width: 160,
    margin: 1,
    color: { dark: BRAND.colors.primary, light: "#ffffff" },
    errorCorrectionLevel: "M",
  });
}

// ── Puppeteer helpers ─────────────────────────────────────────────────────────
let _browser;
async function getBrowser() {
  if (!_browser) {
    _browser = await puppeteer.launch({
      headless: true,
      args: ["--no-sandbox", "--disable-setuid-sandbox"],
    });
  }
  return _browser;
}

async function ensureDir(dir) {
  await mkdir(dir, { recursive: true });
}

/**
 * Render an HTML string to a PDF using Puppeteer.
 * @param {string} html       — fully-resolved HTML string
 * @param {string} pdfPath    — output PDF path
 * @param {object} pdfOpts    — Puppeteer pdf() options override
 * @param {string} tmpSuffix  — unique suffix for the temp HTML file
 */
async function renderHtmlToPdf(html, pdfPath, pdfOpts = {}, tmpSuffix = "tmp") {
  const tmpPath = join(OUTPUT_DIR, `_tmp_rfq_${tmpSuffix}.html`);
  await writeFile(tmpPath, html, "utf-8");

  const browser = await getBrowser();
  const page = await browser.newPage();
  await page.goto(pathToFileURL(tmpPath).toString(), {
    waitUntil: "networkidle0",
  });

  const defaultOpts = {
    format: "Letter",
    printBackground: true,
    preferCSSPageSize: false,
    margin: { top: "1.0in", right: "0.75in", bottom: "0.9in", left: "1.0in" },
  };
  await page.pdf({ path: pdfPath, ...defaultOpts, ...pdfOpts });
  await page.close();
  unlinkSync(tmpPath);
  console.log(`  ✓  ${pdfPath.replace(ROOT + "/", "")}`);
}

