// ── Slug helper ───────────────────────────────────────────────────────────────
function slugify(str) {
  return (str || "rfq")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function escapeHtml(str) {
  return String(str)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

// ── Main generation flow ──────────────────────────────────────────────────────
async function generate() {
  await ensureDir(OUTPUT_DIR);

  const slug = slugify(CONFIG.projectName || CONFIG.rfqNumber || "rfq");
  const pdfParts = [];

  console.log(`\n📄 Generating RFQ package: ${CONFIG.projectName}`);
  console.log(`   Issuing org: ${CONFIG.issuingOrg}`);
  console.log(`   RFQ number:  ${CONFIG.rfqNumber}`);
  console.log(`   Output dir:  ${OUTPUT_DIR}`);
  console.log("");

  // ── 1. Title page ───────────────────────────────────────────────────────────
  console.log("1/N  Title page…");
  const titleRaw = await readFile(
    join(TEMPLATES_DIR, "rfq-title-page.html"),
    "utf-8",
  );
  const titleHtml = applyTokens(titleRaw);
  const titlePdfPath = join(OUTPUT_DIR, `rfq-${slug}-title.pdf`);
  await renderHtmlToPdf(
    titleHtml,
    titlePdfPath,
    { margin: { top: 0, right: 0, bottom: 0, left: 0 } },
    "title",
  );
  pdfParts.push(titlePdfPath);

  // ── 2. Homepage QR for footer (shared across all body pages) ───────────────
  const homepageQr = await buildQrDataUrl(SITE_URL);
  const headerHtml = buildRfqHeaderHtml();
  const footerHtml = buildRfqFooterHtml(homepageQr);

  const bodyPdfOpts = {
    displayHeaderFooter: true,
    headerTemplate: headerHtml,
    footerTemplate: footerHtml,
    margin: { top: "1.0in", right: "0.75in", bottom: "0.9in", left: "1.0in" },
  };

  const bodyRaw = await readFile(join(TEMPLATES_DIR, "rfq-body.html"), "utf-8");

  // ── 3. TOC page (if evaluation criteria present) ────────────────────────────
  if (CONFIG.evaluationCriteria && CONFIG.evaluationCriteria.length > 0) {
    console.log("2/N  Evaluation criteria / TOC…");
    const toc = buildTocContent(CONFIG.evaluationCriteria);
    const tocBodyHtml = applyTokens(
      bodyRaw
        .replaceAll("{{RFQ_SECTION_LABEL}}", toc.label)
        .replaceAll("{{RFQ_SECTION_TITLE}}", toc.title)
        .replaceAll("{{RFQ_SECTION_BODY}}", toc.body),
    );
    const tocPdfPath = join(OUTPUT_DIR, `rfq-${slug}-toc.pdf`);
    await renderHtmlToPdf(tocBodyHtml, tocPdfPath, bodyPdfOpts, "toc");
    pdfParts.push(tocPdfPath);
  }

  // ── 4. Body sections ────────────────────────────────────────────────────────
  const selectedSections = CONFIG.sections || [
    "company-overview",
    "scope",
    "experience",
    "personnel",
    "safety",
    "accreditations",
    "consultation",
  ];

  for (let i = 0; i < selectedSections.length; i++) {
    const sectionId = selectedSections[i];
    const section = buildSectionContent(sectionId);
    if (!section) {
      console.warn(`  ⚠  Unknown section id: ${sectionId} — skipped`);
      continue;
    }
    console.log(`${i + 3}/N  Section: ${section.title}…`);
    const sectionHtml = applyTokens(
      bodyRaw
        .replaceAll("{{RFQ_SECTION_LABEL}}", section.label)
        .replaceAll("{{RFQ_SECTION_TITLE}}", section.title)
        .replaceAll("{{RFQ_SECTION_BODY}}", section.body),
    );
    const sectionPdfPath = join(
      OUTPUT_DIR,
      `rfq-${slug}-section-${sectionId}.pdf`,
    );
    await renderHtmlToPdf(
      sectionHtml,
      sectionPdfPath,
      bodyPdfOpts,
      `section-${sectionId}`,
    );
    pdfParts.push(sectionPdfPath);
  }

  // ── 5. Reference sheet ──────────────────────────────────────────────────────
  console.log("N-1  Reference sheet…");
  const refRows = await buildReferenceRows();
  const refRaw = await readFile(
    join(TEMPLATES_DIR, "rfq-reference-sheet.html"),
    "utf-8",
  );
  const refHtml = applyTokens(
    refRaw.replaceAll("{{RFQ_REFERENCE_ROWS}}", refRows),
  );
  const refPdfPath = join(OUTPUT_DIR, `rfq-${slug}-references.pdf`);
  await renderHtmlToPdf(refHtml, refPdfPath, bodyPdfOpts, "references");
  pdfParts.push(refPdfPath);

  // ── 6. Exhibits ─────────────────────────────────────────────────────────────
  const exhibits = CONFIG.exhibits || [];
  if (exhibits.length > 0) {
    const exhibitRaw = await readFile(
      join(TEMPLATES_DIR, "rfq-exhibit-divider.html"),
      "utf-8",
    );

    for (const exhibit of exhibits) {
      console.log(`N+   Exhibit ${exhibit.id}: ${exhibit.label}…`);
      // Exhibit divider page
      const dividerHtml = applyTokens(exhibitRaw, {
        "{{RFQ_EXHIBIT_ID}}": `Exhibit ${exhibit.id}`,
        "{{RFQ_EXHIBIT_LABEL}}": exhibit.label || "",
        "{{RFQ_EXHIBIT_DESCRIPTION}}": exhibit.description || "",
      });
      const dividerPdfPath = join(
        OUTPUT_DIR,
        `rfq-${slug}-exhibit-${exhibit.id}-divider.pdf`,
      );
      await renderHtmlToPdf(
        dividerHtml,
        dividerPdfPath,
        { margin: { top: 0, right: 0, bottom: 0, left: 0 } },
        `exhibit-${exhibit.id}-div`,
      );
      pdfParts.push(dividerPdfPath);

      // If an exhibit PDF file is specified, try to include it
      if (exhibit.file) {
        const exhibitSrc = exhibitsDir
          ? join(resolve(exhibitsDir), exhibit.file)
          : resolve(exhibit.file);

        try {
          await access(exhibitSrc);
          pdfParts.push(exhibitSrc);
          console.log(`       + appended: ${basename(exhibitSrc)}`);
        } catch {
          console.warn(
            `  ⚠  Exhibit file not found, divider only: ${exhibitSrc}`,
          );
        }
      }
    }
  }

  // ── 7. Merge all PDFs ───────────────────────────────────────────────────────
  console.log("\n📎 Merging PDFs…");
  const merged = await PDFDocument.create();
  for (const pdfPath of pdfParts) {
    try {
      const srcBytes = await readFile(pdfPath);
      const srcDoc = await PDFDocument.load(srcBytes);
      const pageIndices = srcDoc.getPageIndices();
      const copiedPages = await merged.copyPages(srcDoc, pageIndices);
      copiedPages.forEach((page) => merged.addPage(page));
    } catch (err) {
      console.error(`  ✗  Failed to merge: ${pdfPath} — ${err.message}`);
    }
  }

  const mergedPath = join(OUTPUT_DIR, `rfq-${slug}.pdf`);
  const mergedBytes = await merged.save();
  await writeFile(mergedPath, mergedBytes);
  console.log(`  ✓  Merged PDF: ${mergedPath.replace(ROOT + "/", "")}`);

  // ── 8. Cover email ──────────────────────────────────────────────────────────
  console.log("\n📧 Generating cover email…");
  const emailHtml = generateCoverEmail();
  const emailPath = join(OUTPUT_DIR, `rfq-${slug}-email.html`);
  await writeFile(emailPath, emailHtml, "utf-8");
  console.log(`  ✓  Cover email: ${emailPath.replace(ROOT + "/", "")}`);

  // ── 9. Clean up intermediate PDFs ──────────────────────────────────────────
  for (const p of pdfParts) {
    if (p !== mergedPath && !p.endsWith(".pdf") === false) {
      // Only remove files we created in the output dir (not exhibit source files)
      if (p.startsWith(OUTPUT_DIR) && p !== mergedPath) {
        try {
          unlinkSync(p);
        } catch {
          /* ignore cleanup errors */
        }
      }
    }
  }

  console.log(`\n✅  RFQ package complete!`);
  console.log(`    PDF:   ${mergedPath.replace(ROOT + "/", "")}`);
  console.log(`    Email: ${emailPath.replace(ROOT + "/", "")}`);
  console.log("");
}

try {
  await generate();
} finally {
  if (_browser) await _browser.close();
}
