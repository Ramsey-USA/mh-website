/** Build TOC page content from evaluation criteria. */
function buildTocContent(evaluationCriteria) {
  if (!evaluationCriteria || evaluationCriteria.length === 0) return null;

  const rows = evaluationCriteria
    .map((item, idx) => {
      const weight = item.weight
        ? ` &mdash; Weight: ${escapeHtml(item.weight)}`
        : "";
      return `<tr>
        <td class="toc-num">${idx + 1}</td>
        <td class="toc-title">${escapeHtml(item.title)}${weight}</td>
      </tr>`;
    })
    .join("\n");

  return {
    label: "Table of Contents",
    title: "Evaluation Criteria",
    body: `
      <p>The following evaluation criteria have been identified in the RFQ. This package
      is organized to address each criterion directly. Please refer to the corresponding
      section for detailed responses.</p>
      <table class="toc-table">
        <thead>
          <tr>
            <th style="width:0.4in;">#</th>
            <th>Evaluation Criterion</th>
          </tr>
        </thead>
        <tbody>
          ${rows}
        </tbody>
      </table>
      <div class="callout-note">
        <strong>Alignment Commitment</strong>
        Each section of this qualification package directly addresses the evaluation
        criteria above. MH Construction has structured this response to make the
        evaluator&rsquo;s review as clear and efficient as possible.
      </div>
    `,
  };
}

